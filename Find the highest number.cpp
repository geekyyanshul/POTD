class Solution {
public:
    int findPeakElement(vector<int>& a) 
    {
          int n=a.size();
        int largest=0;
        
        for(int i=0;i<n;i++)
        {
            if(a[i]>largest)
            largest=a[i];
        }

        return largest;
    }
};



